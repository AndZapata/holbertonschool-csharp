using System;

namespace MyMath
{
    /// <summary> New public class operations. </summary>
    public class Operations
    {
        /// <summary> Public function to add two integers.</summary>
        /// <param name="a">first integer.</param>
        /// <param name="b">second integer.</param>
        /// <returns>the sum of the two given integers.</returns>
        public static int Add(int a, int b)
        {
            return a + b;
        }
    }
}
